//JavaScript data types
 
//1._number type:
let myAge = 21;
console.log(myAge);
console.log(typeof(myAge));

let myDecimalNumber = 21.5;
console.log(myDecimalNumber);
console.log(typeof(myDecimalNumber));

//2._String type:
let myName = "Example Name";
console.log(myName);
console.log(typeof(myName));

//3._boolean type or logic type: 
let True = true;
console.log(True);
console.log(typeof(True));

let False = false;
console.log(False);
console.log(typeof(False));

//4._undefined type:
let Undefined = undefined;
console.log(Undefined);
console.log(typeof(Undefined));

//5._ null type.
let Null = null;
console.log(Null);
console.log(typeof(Null));


