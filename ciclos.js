{
  //ciclos while:
  //primero evalua la condición y después ejecuta un ciclo

  let contador = 0;
  while (contador < 5) {
    contador = contador += 1;
    console.log("Contador");
  }
}



{
  //ciclos do while
  //se ejecuta al menos una vez y después evalúa la condición

  let contador = 0;
  do {
    contador = contador += 1;
    console.log("contador");
  } while (contador <= 10);
}
